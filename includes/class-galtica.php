<?php

/**
 * The file that defines the core plugin class
 *
 * A class definition that includes attributes and functions used across both the
 * public-facing side of the site and the dashboard.
 *
 * @link       http://example.com
 * @since      1.0.0
 *
 * @package    Galtica
 * @subpackage Galtica/includes
 */

/**
 * The core plugin class.
 *
 * This is used to define internationalization, dashboard-specific hooks, and
 * public-facing site hooks.
 *
 * Also maintains the unique identifier of this plugin as well as the current
 * version of the plugin.
 *
 * @since      1.0.0
 * @package    Galtica
 * @subpackage Galtica/includes
 * @author     Your Name <email@example.com>
 */
class Galtica {

	/**
	 * The loader that's responsible for maintaining and registering all hooks that power
	 * the plugin.
	 *
	 * @since    1.0.0
	 * @access   protected
	 * @var      Galtica_Loader    $loader    Maintains and registers all hooks for the plugin.
	 */
	protected $loader;

	/**
	 * The unique identifier of this plugin.
	 *
	 * @since    1.0.0
	 * @access   protected
	 * @var      string    $plugin_name    The string used to uniquely identify this plugin.
	 */
	protected $plugin_name;

	/**
	 * The current version of the plugin.
	 *
	 * @since    1.0.0
	 * @access   protected
	 * @var      string    $version    The current version of the plugin.
	 */
	protected $version;

	/**
	 * Define the core functionality of the plugin.
	 *
	 * Set the plugin name and the plugin version that can be used throughout the plugin.
	 * Load the dependencies, define the locale, and set the hooks for the Dashboard and
	 * the public-facing side of the site.
	 *
	 * @since    1.0.0
	 */
	public function __construct() {

		$this->plugin_name = 'galtica';
		$this->version = '1.0.0';

		$this->load_dependencies();
		$this->set_locale();
		$this->define_admin_hooks();
		$this->define_public_hooks();
        $this->define_menus();
		$this->define_galtica_settings();
        $this->galtica_filter_cpt();
		/*$this->register_galtica_cpt();
		$this->register_galtica_metabox();
		$this->saver_galtica_cpt();*/
		$this->register_galtica_cpt_simple_content();
		$this->define_content_filter();

	}

	/**
	 * Load the required dependencies for this plugin.
	 *
	 * Include the following files that make up the plugin:
	 *
	 * - Galtica_Loader. Orchestrates the hooks of the plugin.
	 * - Galtica_i18n. Defines internationalization functionality.
	 * - Galtica_Admin. Defines all hooks for the dashboard.
	 * - Galtica_Public. Defines all hooks for the public side of the site.
	 *
	 * Create an instance of the loader which will be used to register the hooks
	 * with WordPress.
	 *
	 * @since    1.0.0
	 * @access   private
	 */
	private function load_dependencies() {

		/**
		 * The class responsible for orchestrating the actions and filters of the
		 * core plugin.
		 */
		require_once plugin_dir_path( dirname( __FILE__ ) ) . 'includes/class-galtica-loader.php';

		/**
		 * The class responsible for defining internationalization functionality
		 * of the plugin.
		 */
		require_once plugin_dir_path( dirname( __FILE__ ) ) . 'includes/class-galtica-i18n.php';

		/**
		 * The class responsible for defining all actions that occur in the Dashboard.
		 */
		require_once plugin_dir_path( dirname( __FILE__ ) ) . 'admin/class-galtica-admin.php';

		/**
		 * The class responsible for defining all actions that occur in the public-facing
		 * side of the site.
		 */
		require_once plugin_dir_path( dirname( __FILE__ ) ) . 'public/class-galtica-public.php';

		$this->loader = new Galtica_Loader();

	}

	/**
	 * Define the locale for this plugin for internationalization.
	 *
	 * Uses the Galtica_i18n class in order to set the domain and to register the hook
	 * with WordPress.
	 *
	 * @since    1.0.0
	 * @access   private
	 */
	private function set_locale() {

		$plugin_i18n = new Galtica_i18n();
		$plugin_i18n->set_domain( $this->get_plugin_name() );

		$this->loader->add_action( 'plugins_loaded', $plugin_i18n, 'load_plugin_textdomain' );

	}

	/**
	 * Register all of the hooks related to the dashboard functionality
	 * of the plugin.
	 *
	 * @since    1.0.0
	 * @access   private
	 */
	private function define_admin_hooks() {

		$plugin_admin = new Galtica_Admin( $this->get_plugin_name(), $this->get_version() );

		$this->loader->add_action( 'admin_enqueue_scripts', $plugin_admin, 'enqueue_styles' );
		$this->loader->add_action( 'admin_enqueue_scripts', $plugin_admin, 'enqueue_scripts' );

	}

	/**
	 * Register all of the hooks related to the public-facing functionality
	 * of the plugin.
	 *
	 * @since    1.0.0
	 * @access   private
	 */
	private function define_public_hooks() {

		$plugin_public = new Galtica_Public( $this->get_plugin_name(), $this->get_version() );

		$this->loader->add_action( 'wp_enqueue_scripts', $plugin_public, 'enqueue_styles' );
		$this->loader->add_action( 'wp_enqueue_scripts', $plugin_public, 'enqueue_scripts' );

	}

	/**
	 * Register filter into the content
	 *
	 */
	private function define_content_filter() {

		$plugin_public = new Galtica_Public( $this->get_plugin_name(), $this->get_version() );

		$this->loader->add_filter( 'the_content', $plugin_public, 'register_content_filter' );

	}

    /**
     * Define menu and pages
     *
     */
    public function define_menus(){

        $plugin_admin = new Galtica_Admin( $this->get_plugin_name(), $this->get_version() );

        $this->loader->add_action( 'admin_menu', $plugin_admin, 'register_galtica_menu_page'  );
    }

	/**
	 * Register our settings
	 *
	 */
	public function define_galtica_settings(){

		$plugin_admin = new Galtica_Admin( $this->get_plugin_name(), $this->get_version() );

		$this->loader->add_action( 'admin_init', $plugin_admin, 'register_galtica_settings'  );
	}

    public function galtica_filter_cpt(){
        $plugin_admin = new Galtica_Admin( $this->get_plugin_name(), $this->get_version() );

        $this->loader->add_filter('acf/load_field/name=galtica_cpt_content', $plugin_admin, 'galtica_filter_acf');
        // $this->loader->add_filter('acf/load_field/name=subnome_para_teste', $plugin_admin, 'galtica_filter_acf');
    }

    /**
     * Register our generic custom post type
     * This will handle all of dynamic content
     *
     */
    public function register_galtica_cpt(){

        $plugin_admin = new Galtica_Admin( $this->get_plugin_name(), $this->get_version() );

        $this->loader->add_action( 'init', $plugin_admin, 'init_galtica_cpt'  );
    }

	/**
	 * Register custom post type - ConteÃºdo Simples
	 *
	 */
	public function register_galtica_cpt_simple_content(){

		$plugin_admin = new Galtica_Admin( $this->get_plugin_name(), $this->get_version() );

		$this->loader->add_action( 'init', $plugin_admin, 'galtica_cpt_simple_content'  );
	}

    /**
     * Register our Galtica custom metabox of data
     *
     */
    public function register_galtica_metabox(){

        $plugin_admin = new Galtica_Admin( $this->get_plugin_name(), $this->get_version() );

        $this->loader->add_action( 'add_meta_boxes', $plugin_admin, 'create_galtica_metabox'  );
    }

    /**
     * Handle the saving action for galtica custom post types
     *
     */
    public function saver_galtica_cpt(){

        $plugin_admin = new Galtica_Admin( $this->get_plugin_name(), $this->get_version() );

        $this->loader->add_action( 'save_post', $plugin_admin, 'galtica_save_cpt'  );
    }

	/**
	 * Run the loader to execute all of the hooks with WordPress.
	 *
	 * @since    1.0.0
	 */
	public function run() {
		$this->loader->run();
	}

	/**
	 * The name of the plugin used to uniquely identify it within the context of
	 * WordPress and to define internationalization functionality.
	 *
	 * @since     1.0.0
	 * @return    string    The name of the plugin.
	 */
	public function get_plugin_name() {
		return $this->plugin_name;
	}

	/**
	 * The reference to the class that orchestrates the hooks with the plugin.
	 *
	 * @since     1.0.0
	 * @return    Galtica_Loader    Orchestrates the hooks of the plugin.
	 */
	public function get_loader() {
		return $this->loader;
	}

	/**
	 * Retrieve the version number of the plugin.
	 *
	 * @since     1.0.0
	 * @return    string    The version number of the plugin.
	 */
	public function get_version() {
		return $this->version;
	}

}
